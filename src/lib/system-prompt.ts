let cachedPrompt: string | null = null;
let promptCacheTime = 0;
const PROMPT_CACHE_TTL = 60_000;

export async function getSystemPromptText(): Promise<string> {
  const now = Date.now();
  if (cachedPrompt && now - promptCacheTime < PROMPT_CACHE_TTL) {
    return cachedPrompt;
  }
  try {
    const [row] = await db
      .select()
      .from(configTable)
      .where(eq(configTable.key, "system_prompt"))
      .limit(1);
    cachedPrompt = row?.value || DEFAULT_SYSTEM_PROMPT;
  } catch {
    cachedPrompt = DEFAULT_SYSTEM_PROMPT;
  }
  promptCacheTime = now;
  return cachedPrompt;
}

export type BuildPromptOptions = {
  scenarioContext?: string;
  knowledgeContext?: string;
  profile?: Record<string, string>;
  covered?: string[];
};

export function buildSystemPrompt(
  basePrompt: string,
  language: string,
  options: BuildPromptOptions = {}
): string {
  const { scenarioContext = "", knowledgeContext = "", profile, covered } = options;
  const languageLabel = getLanguageLabel(language);

  const langInstruction = `\n\n══════════════════════════════════════════════
ЕЗИК НА ОТГОВОРА / RESPONSE LANGUAGE (ЗАДЪЛЖИТЕЛНО)
══════════════════════════════════════════════

Езикът на текущото потребителско съобщение е "${languageLabel}". ТРЯБВА да отговаряш ИЗЦЯЛО на същия език. Преведи всички съвети, заглавия и обяснения. Запази непроменени: собствени имена на институции, URLs, адреси.`;

  const playbookInstruction = scenarioContext
    ? `\n\n══════════════════════════════════════════════
РАЗПОЗНАТ СЦЕНАРИЙ — КАК ДА ГО ПОЛЗВАШ
══════════════════════════════════════════════

За разговора е разпознат поне един СЦЕНАРИЙ (по-долу). Той е твоят tier-1 ориентир за тази тема:
• Следвай „ПОВЕДЕНИЕ И ТОН" и „ПРЕПОРЪЧИТЕЛЕН ПОДХОД" — но подходът е препоръка, нагаждай се към разговора и НЕ повтаряй вече изяснено.
• Ползвай „ИНФОРМАЦИЯ ПО СЪЩЕСТВО" за фактите и разклоненията според профила.
• Приоритизирай „НАСОЧВАЩИ РЕСУРСИ" (tier-1) пред общите ресурси по-долу. Води по съдържанието; давай само къси линкове при нужда.
• Спазвай профил-първо: ако липсва нужен детайл, първо изясни, после отговори.`
    : "";

  const profileBlock =
    profile && Object.keys(profile).length > 0
      ? `\n\n══════════════════════════════════════════════
ИЗВЕСТЕН ПРОФИЛ НА ПОТРЕБИТЕЛЯ (не питай повторно за тези данни)
══════════════════════════════════════════════

${Object.entries(profile)
          .map(([k, v]) => `• ${profileLabel(k)}: ${v}`)
          .join("\n")}`
      : "";

  const coveredBlock =
    covered && covered.length > 0
      ? `\n\nВЕЧЕ ИЗЯСНЕНО В РАЗГОВОРА (не питай отново, продължи оттук):\n${covered
          .map((c) => `• ${c}`)
          .join("\n")}`
      : "";

  const scenarioBlock = scenarioContext
    ? `\n\n══════════════════════════════════════════════
СЦЕНАРИЙ / PLAYBOOK (tier-1)
══════════════════════════════════════════════

${scenarioContext}`
    : "";

  const resourceBlock = knowledgeContext
    ? `\n\n══════════════════════════════════════════════
ДОПЪЛНИТЕЛНИ РЕСУРСИ (tier-2 — резерва от базата знания)
══════════════════════════════════════════════

Ползвай ги като допълнение, когато сценарият не покрива въпроса. Вграждай информацията в отговора; давай само къси линкове при нужда. Помни: ukraine.gov.bg е остарял (последен избор).\n\n${knowledgeContext}`
    : "";

  return (
    basePrompt +
    langInstruction +
    playbookInstruction +
    profileBlock +
    coveredBlock +
    scenarioBlock +
    resourceBlock
  );
}

function profileLabel(key: string): string {
  switch (key) {
    case "origin":
      return "Произход";
    case "status":
      return "Статут";
    case "location":
      return "Локация";
    case "age":
      return "Възраст";
    case "documents":
      return "Документи";
    case "notes":
      return "Друго";
    default:
      return key;
  }
}

function getLanguageLabel(language: string): string {
  switch (language) {
    case "bg":
      return "Bulgarian";
    case "en":
      return "English";
    case "ua":
      return "Ukrainian";
    case "fa":
      return "Persian (Farsi)";
    case "ar":
      return "Arabic";
    default:
      return language;
  }
}

