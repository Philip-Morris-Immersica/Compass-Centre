let cachedPrompt: string | null = null;
let promptCacheTime = 0;
const PROMPT_CACHE_TTL = 60_000;

export async function getSystemPromptText(): Promise<string> {
  const now = Date.now();
  if (cachedPrompt && now - promptCacheTime < PROMPT_CACHE_TTL) {
    return cachedPrompt;
  }
  try {
    const [row] = await db
      .select()
      .from(configTable)
      .where(eq(configTable.key, "system_prompt"))
      .limit(1);
    cachedPrompt = row?.value || DEFAULT_SYSTEM_PROMPT;
  } catch {
    cachedPrompt = DEFAULT_SYSTEM_PROMPT;
  }
  promptCacheTime = now;
  return cachedPrompt;
}

export function buildSystemPrompt(basePrompt: string, language: string, knowledgeContext: string): string {
  const languageLabel = getLanguageLabel(language);
  const langInstruction = `\n\n══════════════════════════════════════════════
6. ЕЗИК НА ОТГОВОРА / RESPONSE LANGUAGE (ЗАДЪЛЖИТЕЛНО)
══════════════════════════════════════════════

Езикът на текущото потребителско съобщение е "${languageLabel}". ТРЯБВА да отговаряш ИЗЦЯЛО на същия език. Преведи всички съвети, заглавия и обяснения. Запази непроменени: собствени имена на институции, URLs, адреси.`;

  const resourceBlock = knowledgeContext
    ? `\n\n══════════════════════════════════════════════
РЕСУРСИ / RESOURCES (Използвай ги като ОСНОВЕН ИЗТОЧНИК)
══════════════════════════════════════════════

По-долу са предоставени ресурси от базата данни на Компас Център. ЗАДЪЛЖИТЕЛНО ги прегледай и използвай при отговора си. Цитирай конкретни части и включвай URL адреси.\n\n${knowledgeContext}`
    : "";

  return basePrompt + langInstruction + resourceBlock;
}

function getLanguageLabel(language: string): string {
  switch (language) {
    case "bg":
      return "Bulgarian";
    case "en":
      return "English";
    case "ua":
      return "Ukrainian";
    case "fa":
      return "Persian (Farsi)";
    case "ar":
      return "Arabic";
    default:
      return language;
  }
}

export function invalidatePromptCache() {
  cachedPrompt = null;
  promptCacheTime = 0;
}
